    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
        ChildClass ItemCost = new ChildClass();
        
        double iTax, iSubTotal, iTotal;
        
        ItemCost.ChickenBurger = ItemCost.pChickenBurger * Double.parseDouble(jtxtChickenBurger.getText());
        ItemCost.BeefBurger = ItemCost.pBeefBurger * Double.parseDouble(jtxtBeefBurger.getText());
        ItemCost.Pizza = ItemCost.pPizza * Double.parseDouble(jtxtPizza.getText());
        ItemCost.ChickenPasta = ItemCost.pChickenPasta * Double.parseDouble(jtxtChickenPasta.getText());
        ItemCost.ShirmpPasta = ItemCost.pShirmpPasta * Double.parseDouble(jtxtShrimpPasta.getText());
        ItemCost.Sandwich = ItemCost.pSandwich * Double.parseDouble(jtextSandwich.getText());
        
        ItemCost.MilkShake = ItemCost.pMilkShake * Double.parseDouble(jtxtMilkShake.getText());
        ItemCost.OreoMilkShake = ItemCost.pOreoMilkShake * Double.parseDouble(jtxtOreoMilkShake.getText());
        ItemCost.VanillaMilkShake = ItemCost.pVanillaMilkShake * Double.parseDouble(jtxtVanillaMilkShake.getText());
        ItemCost.ChocolateMilkShake = ItemCost.pChocolateMilkShake * Double.parseDouble(jtxtChocolateMilkShake.getText());
        ItemCost.ClassicVanilla = ItemCost.pClassicVanilla * Double.parseDouble(jtxtClassicVanilla.getText());
        ItemCost.RaspberryMilkShake = ItemCost.pRaspberryMilkShake * Double.parseDouble(jtxtRaspberryMilkshake.getText());
        
        iSubTotal = ItemCost.GetAmount();
        iTax = ItemCost.cFindTax(iSubTotal);
        iTotal = iSubTotal + iTax;
        
        String SubTotal = String.format("%.2f", iSubTotal);
        jtxtSubTotal.setText(SubTotal);
        
         String Tax = String.format("%.2f", iTax);
        jtxtTax.setText(Tax);
        
         String Total = String.format("%.2f", iTotal);
        jtxtTotal.setText(Total);
        
        String Meal =String.format("%.2f", ItemCost.Meals);
        jtxtCostofMeal.setText(Meal);
        
        String Drink =String.format("%.2f", ItemCost.Drinks);
        jtxtCostofDrinks.setText(Drink);
        
        String TotalCost =String.format("%.2f", ItemCost.TotalofMD);
        jtxtTotalCost.setText(TotalCost);
        
    }//GEN-LAST:event_jbtnTotalActionPerformed

